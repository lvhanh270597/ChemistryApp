public class ktk {
    private String kettua;
    private String khi;
    public ktk(){}
    public ktk(String X, String Y){
        this.kettua = X;
        this.khi = Y;
    }
    public String getKT(){
        return this.kettua;
    }
    public String getKhi(){
        return this.khi;
    }
}
